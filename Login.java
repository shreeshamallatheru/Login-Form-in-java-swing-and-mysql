    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        // Checking for empty username
        if(jTextField1.getText().length()==0)                                   
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up username");
        // Checking for empty password
        else if(jPasswordField1.getPassword().length==0)  
            JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up password");
        else{
            
            //Stores  the username and password in variables
            String username = jTextField1.getText();
            char[] password = jPasswordField1.getPassword();
            
            //Converts password to string format
            String passwordString = String.copyValueOf(password);
          
            //Validates the username and password
            if(validate_login(username,passwordString))
                JOptionPane.showMessageDialog(null, "Correct Login Credentials");        
            else
                JOptionPane.showMessageDialog(null, "Incorrect Login Credentials");
        }    
    }//GEN-LAST:event_jButton1ActionPerformed


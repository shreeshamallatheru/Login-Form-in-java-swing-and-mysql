    //Method validates the username & password
    private boolean validate_login(String username, String passwordString) {
        try{           
            Class.forName("com.mysql.jdbc.Driver");  // MySQL database connection
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/login" ,"root","Iamgood@25*#");     
            PreparedStatement pst = (PreparedStatement) conn.prepareStatement("Select * from login where username=? and password=?");
            pst.setString(1, username); 
            pst.setString(2, passwordString);
            ResultSet rs = pst.executeQuery();                        
            if(rs.next())            
                return true;    
            else
                return false;            
        }
        catch(Exception e){
            e.printStackTrace();
            return false;
        }       
    }
